package vo;

public class Movie {
	private String movieName;
	private String time;
	private String country;
	private String actor;
	private String moviekind;
	private String director;
	private Double grade;
	private Double numOfPeople;
	private String screenwriter;
	private Integer price;
	public String getMovieName() {
		return movieName;
	}
	public void setMovieName(String movieName) {
		this.movieName = movieName;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getActor() {
		return actor;
	}
	public void setActor(String actor) {
		this.actor = actor;
	}
	public String getMoviekind() {
		return moviekind;
	}
	public void setMoviekind(String moviekind) {
		this.moviekind = moviekind;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public Double getGrade() {
		return grade;
	}
	public void setGrade(Double grade) {
		this.grade = grade;
	}
	public String getScreenwriter() {
		return screenwriter;
	}
	public void setScreenwriter(String screenwriter) {
		this.screenwriter = screenwriter;
	}
	public Double getNumOfPeople() {
		return numOfPeople;
	}
	public void setNumOfPeople(Double numOfPeople) {
		this.numOfPeople = numOfPeople;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	
}
